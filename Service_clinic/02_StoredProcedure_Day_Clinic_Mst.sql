	@PH_PNC_FLG char = 'N',
	@PH_VAC_FLG char = 'N',
	@PH_RNTCP_FLG char = 'N',	
	@PH_NLEP_FLG char = 'N',
	@BASE_LVL_REF_CD nvarchar,	
	@LVL_REF_CD nvarchar ,	
	@ACTIVE_FLG char = 'Y',	
	@ENTRY_DT date ,
	@USER_CD nvarchar,
	@SESSION_ID Numeric,
	@MOD_ID nvarchar,
	@SUB_MOD_ID nvarchar,
	@ACT_FLG char = 'A',	
	@MOD_DT date,	
	@SESSION_ID_WEB nvarchar,
	@QueryType nvarchar = 'Insert')
AS
	BEGIN
		SET NOCOUNT ON;
		IF @QueryType = 'Insert'
		BEGIN 
		INSERT INTO [dbo].[DAY_CLINIC_MST]
		([HOS_CD],
		[DAY_CLINIC_CD],
		[DAY_CLINIC_NM],
		[DAY_CLINIC_TYP_CD],
		[UNIT_DEPD_FLG],
		[MON_FLG],
		[TUE_FLG],
		[WED_FLG],
		[THU_FLG],
		[FRI_FLG],
		[SAT_FLG],	
		[SUN_FLG],	
		[MON_FR_TM],	
		[MON_TO_TM],	
		[TUE_FR_TM],	
		[TUE_TO_TM],
		[WED_FR_TM],	
		[WED_TO_TM],	
		[THU_FR_TM],
		[THU_TO_TM],	
		[FRI_FR_TM],
		[FRI_TO_TM],	
		[SAT_FR_TM],
		[SAT_TO_TM],	
		[SUN_FR_TM],	
		[SUN_TO_TM,	
		[MON_VENUE_NM],	
		[TUE_VENUE_NM],	
		[WED_VENUE_NM],		
		[THU_VENUE_NM],	
		[FRI_VENUE_NM],	
		[SAT_VENUE_NM],	
		[SUN_VENUE_NM],	
