				[TUE_VENUE_NM] = @TUE_VENUE_NM,
				[WED_VENUE_NM] = @WED_VENUE_NM,
				[THU_VENUE_NM] = @THU_VENUE_NM,
				[FRI_VENUE_NM] = @FRI_VENUE_NM,
				[SAT_VENUE_NM] = @SAT_VENUE_NM,
				[SUN_VENUE_NM] = @SUN_VENUE_NM,
				[PH_ANC_FLG] = @PH_ANC_FLG,
				[PH_DLV_FLG] = @PH_DLV_FLG,
				[PH_PNC_FLG] = @PH_PNC_FLG,
				[PH_VAC_FLG] = @PH_VAC_FLG,
				[PH_RNTCP_FLG] = @PH_RNTCP_FLG,	
				[PH_NLEP_FLG] = @PH_NLEP_FLG,
				[BASE_LVL_REF_CD] =@BASE_LVL_REF_CD,	
				[LVL_REF_CD] = @LVL_REF_CD,	
				[ACTIVE_FLG] = @ACTIVE_FLG,
				[ENTRY_DT] = @ENTRY_DT,
				[USER_CD] = @USER_CD,
				[SESSION_ID] = @SESSION_ID,
				[MOD_ID] = @MOD_ID,
				[SUB_MOD_ID] = @SUB_MOD_ID,
				[ACT_FLG] = @ACT_FLG,	
				[MOD_DT] = @MOD_DT,	
				[SESSION_ID_WEB] = @SESSION_ID_WEB
				WHERE [HOS_CD] = @HOS_CD AND [DAY_CLINIC_CD] = @DAY_CLINIC_CD;
			END

	ELSE IF @QueryType = 'Delete'
		BEGIN
		DELETE FROM [dbo].[DAY_DOC_SPL]
			WHERE [HOS_CD] = @HOS_CD AND [DAY_CLINIC_CD] = @DAY_CLINIC_CD;
	END
END
