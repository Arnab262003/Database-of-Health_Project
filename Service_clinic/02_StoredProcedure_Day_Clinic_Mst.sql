				[WED_TO_TM] = @WED_TO_TM,
				[THU_FR_TM] = @THU_FR_TM,
				[THU_TO_TM] = @THU_TO_TM,	
				[FRI_FR_TM] = @FRI_FR_TM,
				[FRI_TO_TM] = @FRI_TO_TM,
				[SAT_FR_TM] = @SAT_FR_TM,
				[SAT_TO_TM] = @SAT_TO_TM,
