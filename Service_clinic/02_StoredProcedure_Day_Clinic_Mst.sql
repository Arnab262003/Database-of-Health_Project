	@MON_FLG nvarchar = 'N' ,	
	@TUE_FLG nvarchar(1) = 'N',
	@WED_FLG nvarchar = 'N' ,
	@THU_FLG nvarchar = 'N' ,
	@FRI_FLG nvarchar = 'N' ,
	@SAT_FLG nvarchar = 'N' ,	
	@SUN_FLG nvarchar = 'N' ,	
	@MON_FR_TM nvarchar,	
	@MON_TO_TM nvarchar,	
	@TUE_FR_TM nvarchar,	
	@TUE_TO_TM nvarchar,
	@WED_FR_TM nvarchar,	
	@WED_TO_TM nvarchar,	
	@THU_FR_TM nvarchar,
	@THU_TO_TM nvarchar,	
	@FRI_FR_TM nvarchar,
	@FRI_TO_TM nvarchar,	
	@SAT_FR_TM nvarchar,
	@SAT_TO_TM nvarchar,	
	@SUN_FR_TM nvarchar,	
	@SUN_TO_TM nvarchar,	
	@MON_VENUE_NM nvarchar,	
	@TUE_VENUE_NM nvarchar,	
	@WED_VENUE_NM nvarchar,		
	@THU_VENUE_NM nvarchar,	
	@FRI_VENUE_NM nvarchar,	
