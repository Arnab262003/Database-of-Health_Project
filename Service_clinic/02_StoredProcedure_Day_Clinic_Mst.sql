		[PH_DLV_FLG],
		[PH_PNC_FLG],
		[PH_VAC_FLG],
		[PH_RNTCP_FLG],	
		[PH_NLEP_FLG],
		[BASE_LVL_REF_CD],	
		[LVL_REF_CD],	
		[ACTIVE_FLG],	
		[ENTRY_DT],
		[USER_CD],
		[SESSION_ID],
		[MOD_ID],
		[SUB_MOD_ID],
		[ACT_FLG],	
		[MOD_DT],	
		[SESSION_ID_WEB]
		)
		VALUES
		  (@HOS_CD ,
		@DAY_CLINIC_CD ,
		@DAY_CLINIC_NM,
		@DAY_CLINIC_TYP_CD,
		@UNIT_DEPD_FLG,
		@MON_FLG,	
		@TUE_FLG,
		@WED_FLG,
		@THU_FLG,
		@FRI_FLG,
		@SAT_FLG,	
		@SUN_FLG,	
		@MON_FR_TM,	
		@MON_TO_TM,	
		@TUE_FR_TM,	
		@TUE_TO_TM,
		@WED_FR_TM,	
		@WED_TO_TM,	
		@THU_FR_TM,
		@THU_TO_TM,	
		@FRI_FR_TM,
		@FRI_TO_TM,	
		@SAT_FR_TM,
		@SAT_TO_TM,	
		@SUN_FR_TM,	
		@SUN_TO_TM,	
		@MON_VENUE_NM,	
		@TUE_VENUE_NM,	
		@WED_VENUE_NM,		
		@THU_VENUE_NM,	
		@FRI_VENUE_NM,	
		@SAT_VENUE_NM,	
		@SUN_VENUE_NM,	
		@PH_ANC_FLG,	
		@PH_DLV_FLG,
		@PH_PNC_FLG,
		@PH_VAC_FLG,
		@PH_RNTCP_FLG,	
		@PH_NLEP_FLG,
		@BASE_LVL_REF_CD,	
		@LVL_REF_CD,	
		@ACTIVE_FLG,	
		@ENTRY_DT ,
		@USER_CD,
		@SESSION_ID,
		@MOD_ID,
		@SUB_MOD_ID,
		@ACT_FLG,	
		@MOD_DT,	
		@SESSION_ID_WEB
		 );
	  IF @@ROWCOUNT = 1
		RETURN 1;
	  ELSE
		RETURN 0;
	END

	ELSE IF @QueryType = 'Update'
		BEGIN 
			UPDATE DAY_CLINIC_MST
				SET 
				[DAY_CLINIC_NM] = @DAY_CLINIC_NM,
				[DAY_CLINIC_TYP_CD] = @DAY_CLINIC_TYP_CD,
				[UNIT_DEPD_FLG] = @UNIT_DEPD_FLG,
				[MON_FLG] = @MON_FLG,
				[TUE_FLG] = @TUE_FLG,
				[WED_FLG] = @WED_FLG,
				[THU_FLG] = @THU_FLG,
				[FRI_FLG] = @FRI_FLG,
				[SAT_FLG] = @SAT_FLG,
				[SUN_FLG] = @SUN_FLG,
